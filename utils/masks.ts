export function maskCPF(value: string) {
  return value
    .replace(/\D/g, "")
    .slice(0, 11)
    .replace(/(\d{3})(\d)/, "$1.$2")
    .replace(/(\d{3})(\d)/, "$1.$2")
    .replace(/(\d{3})(\d{1,2})$/, "$1-$2");
}

export function maskCNPJ(value: string) {
  return value
    .replace(/\D/g, "")
    .slice(0, 14)
    .replace(/^(\d{2})(\d)/, "$1.$2")
    .replace(/^(\d{2})\.(\d{3})(\d)/, "$1.$2.$3")
    .replace(/\.(\d{3})(\d)/, ".$1/$2")
    .replace(/(\d{4})(\d)/, "$1-$2");
}

export function applyCpfCnpjMask(value: string, tipoPessoa: string) {
  const digits = value.replace(/\D/g, "");

  if (tipoPessoa === "F") {
    return maskCPF(digits);
  }

  return maskCNPJ(digits);
}

export function maskTelefone(value: string) {
  const digits = value.replace(/\D/g, "").slice(0, 11);

  if (digits.length <= 10) {
    // (XX) XXXX-XXXX
    return digits.replace(
      /^(\d{2})(\d{0,4})(\d{0,4})$/,
      (_, ddd, p1, p2) => `(${ddd}) ${p1}${p2 ? "-" + p2 : ""}`,
    );
  }

  // (XX) X XXXX-XXXX
  return digits.replace(
    /^(\d{2})(\d{1})(\d{0,4})(\d{0,4})$/,
    (_, ddd, d9, p1, p2) => `(${ddd}) ${d9} ${p1}${p2 ? "-" + p2 : ""}`,
  );
}
